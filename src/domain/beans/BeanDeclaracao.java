package domain.beans;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;

import domain.basics.Declaracao;
import domain.basics.Item;
import domain.facade.Fachada;

@ManagedBean
@SessionScoped
public class BeanDeclaracao {
	
////////////////////////////////////////////////////////atributos/////////////////////////////////////////////////////////////////////////		
	private Fachada fachada;
	private Declaracao declaracao;
	private Item item;
////////////////////////////////////////////////////////atributos/////////////////////////////////////////////////////////////////////////
	
	
////////////////////////////////////////////////////////construtor/////////////////////////////////////////////////////////////////////////
	public BeanDeclaracao(){
		this.fachada = new Fachada();
	}
////////////////////////////////////////////////////////construtor/////////////////////////////////////////////////////////////////////////
	
	
	
	
	
	
	
	
////////////////////////////////////////////////////////gets e sets/////////////////////////////////////////////////////////////////////////
	public Declaracao getDeclaracao() {
		return declaracao;
	}

	public void setDeclaracao(Declaracao declaracao) {
		this.declaracao = declaracao;
	}

	public Item getItem() {
		return item;
	}

	public void setItem(Item item) {
		this.item = item;
	}

	public Fachada getFachada() {
		return fachada;
	}
////////////////////////////////////////////////////////gets e sets/////////////////////////////////////////////////////////////////////////
}
